public class uso_bucle_for {
    public static void main(String[] args) {
        for(int i=0;i<10;i++){
            System.out.println("Emmanuel");
            System.out.println("Elias");
        }
        for(int z=10;z>0;z--){
            System.out.println("Elias Agustin Delicia");
        }
    }
    
}
